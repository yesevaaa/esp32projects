void nine();

const int a = 33;
const int b = 32;
const int c = 19;
const int d = 22;
const int e = 23;
const int f = 25;
const int g = 26;
const int dot = 18;
int count = 0;
ezButton button(4);


void setup() {
  Serial.begin(9600);
  button.setDebounceTime(50);
  pinMode(a, OUTPUT);
  pinMode(b, OUTPUT);
  pinMode(c, OUTPUT);
  pinMode(d, OUTPUT);
  pinMode(e, OUTPUT);
  pinMode(f, OUTPUT);
  pinMode(g, OUTPUT);
  pinMode(dot, OUTPUT);
  Serial.println(count);
}

void loop() {
  button.loop();
  if (button.isPressed()) {
    if (count <= 9 ) {
      count++;
      if (count > 9) {
        count = 0;
      }
    }
    Serial.println(count);
  }

  switch (count) {
    case 0:
      zero();
      break;
    case 1:
      one();
      break;
    case 2:
      two();
      break;
    case 3:
      three();
      break;
    case 4:
      four();
      break;
    case 5:
      five();
      break;
    case 6:
      six();
      break;
    case 7:
      seven();
      break;
    case 8:
      eight();
      break;
    case 9:
      nine();
      break;
  }

}

